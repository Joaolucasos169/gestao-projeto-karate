import express from 'express'
import cors from 'cors'
import dotenv from 'dotenv'
import authRoutes from './routes/auth.js'
import studentsRoutes from './routes/students.js'

dotenv.config()
const app = express()

// âœ… ConfiguraÃ§Ã£o de CORS (liberada para ambiente local)
app.use(cors({
  origin: ['http://localhost:5173', 'http://127.0.0.1:5173'], // Libera o frontend local
  methods: ['GET', 'POST', 'PUT', 'DELETE', 'OPTIONS'],
  allowedHeaders: ['Content-Type', 'Authorization']
}))

// âœ… Habilita JSON
app.use(express.json())

// âœ… Rota de teste (verificar se API estÃ¡ online)
app.get('/health', (req, res) => res.json({ ok: true }))

// âœ… Rotas principais
app.use('/auth', authRoutes)
app.use('/students', studentsRoutes)

// âœ… Tratamento bÃ¡sico de erro (evita travamentos)
app.use((err, req, res, next) => {
  console.error('ðŸš¨ Erro no servidor:', err)
  res.status(500).json({ error: 'Erro interno do servidor.' })
})

// âœ… InicializaÃ§Ã£o
const PORT = process.env.PORT || 3000
app.listen(PORT, () => {
  console.log(`âœ… API rodando em http://localhost:${PORT}`)
})
